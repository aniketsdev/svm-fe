import type { FocusEvent, ReactNode } from 'react';

/**
 * The shape every form-input component in `src/common/` extends.
 *
 * `TValue` is the value type of the input (e.g. `string`, `boolean`, `Date`,
 * `File | null`, `string[]`). Component-specific props (e.g. `options`,
 * `accept`, `displayFormat`) extend this interface with their own additions.
 *
 * Defined once here so every input renders the same error / label / helper
 * surface and exposes the same identity props (`name`, `id`).
 *
 * See `specs/001-mui-to-tailwind-migration/data-model.md` §3.
 */
export interface BaseFormInputProps<TValue> {
  /** HTML `name` attribute; required for forms and RHF integration. */
  name: string;
  /** HTML `id`; auto-generated by the input if not provided. */
  id?: string;

  /** Field label rendered above the control. */
  label?: ReactNode;
  /** Hint text rendered below the control when there is no error. */
  helperText?: ReactNode;
  /** Native placeholder forwarded to the control when applicable. */
  placeholder?: string;

  /** Controlled value. */
  value?: TValue;
  /** Uncontrolled initial value. */
  defaultValue?: TValue;
  /** Fired with the next value when the user changes the input. */
  onChange?: (next: TValue) => void;
  /** Fired when the control loses focus (passed through to RHF). */
  onBlur?: (event: FocusEvent<HTMLElement>) => void;

  /**
   * Error indicator. A truthy string renders as the error message below the
   * field; a truthy boolean marks the field invalid without a message.
   */
  error?: string | boolean;
  /** Visual `*` marker; does NOT enforce validation — that lives in the Zod schema. */
  required?: boolean;

  /** Standard disabled flag. */
  disabled?: boolean;
  /** Read-only flag (where supported by the underlying control). */
  readOnly?: boolean;
  /** Auto-focus on mount. */
  autoFocus?: boolean;

  /** Tailwind classes applied to the component's root wrapper. */
  className?: string;
}
